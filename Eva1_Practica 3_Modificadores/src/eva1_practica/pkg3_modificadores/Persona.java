public class Persona {
 private String nombre;
 private String apellido;
 private int edad;
 private String genero;
 private String curp;
 
 public String get_Nombre(){
     return nombre;
 }
 public void set_Nombre(String valor){
     nombre = valor;
 }
 public String get_Apellido(){
     return apellido;
 }
 public void set_Apellido(String valor){
     apellido = valor;
 }
 public int get_Edad(){
     return edad;
 }
 public void set_Edad(int valor){
     edad = valor;
 }
 public String get_Genero(){
     return genero;
 }
 public void set_Genero(String valor){
     genero = valor;
 }
 public String get_Curp(){
     return curp;
 }
 public void set_Curp(String valor){
     curp = valor;
 }
 
 
 
 
 
 
}
